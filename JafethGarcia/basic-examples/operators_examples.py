def operators():
    a = 10
    b = 20

    print('a + b =', a + b)
    print('a - b =', a - b)
    print('a - b =', a * b)
    print('b / a =', b / a)
    print('b % a =', b % a)
    print('a**b =', a**b)

    print('9//2 =', 9//2)
    print('9.0//2.0 =', 9.0//2.0)
    print('-11//3 =', 9//-4)
    print('-11.0//3 =', 9//-4.0)


operators()
